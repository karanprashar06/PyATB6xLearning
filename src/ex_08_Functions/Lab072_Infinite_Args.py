def print_mul_arg(*Karan_list):
    # args - List
    for i in Karan_list:
        print(i)


print_mul_arg("Karan")
print_mul_arg(2, 3, 1, 4, 3, 2, 2, 2, 2, 2, 2)
print_mul_arg("KARAN","Prashar")
print_mul_arg("KARAN","Prashar", "third")
print_mul_arg("KARAN","Prashar", "third", 3.14)
print_mul_arg("KARAN","Prashar", "third", 3.14, True)